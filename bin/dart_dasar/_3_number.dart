/// Number = TipeData angka => int, double
/// int => TipeData bilangan bulat
/// double => TipeData bilangan desimal
void main() {
  int angka = 10;
  double desimal = 10.5;
  print(angka);
  print(desimal);

  /// TipeData num => bisa int dan double
  num angka2 = 10; // int
  print(angka2);
  angka2 = 10.5; // double
  print(angka2);
}
