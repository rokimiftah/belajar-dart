/// Operator aritmatika
/// + => pertambahan
/// -, -expression => pengurangan atau negatif
/// \* => perkalian
/// / => pembagian
/// ~/ => pembagian dengan pembulatan ke bawah
/// % => modulus
void main() {
  var result1 = 10 + 3;
  var result2 = 10 - 3;
  var result3 = 10 * 3;
  var result4 = 10 / 3;
  var result5 = 10 ~/ 3;
  var result6 = 10 % 3;
  print(result1);
  print(result2);
  print(result3);
  print(result4);
  print(result5);
  print(result6);
}
