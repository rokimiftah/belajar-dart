/// Operator perbandingan adalah operator yang hasilnya bool
/// Operator perbandingan bisa digunakan di tipe data number dan string
///   == => sama dengan
/// ` != => tidak sama dengan
///   > => lebih dari
///   < => kurang dari
///  >= => lebih dari sama dengan
///  <= => kurang dari sama dengan
void main() {
  print(10 == 10);
  print(10 != 10);
  print(10 > 10);
  print(10 < 10);
  print(10 >= 10);
  print(10 <= 10);

  print('A' == 'A');
  print('A' != 'A');
}
